package dataTypes;

public final class DataTypes {
    public static void main(String[] args) {
        primitiveTypes();
        referenceTypes();
    }

    private static void primitiveTypes() {
        // Primitive Types
        byte myByte = 6;
        short myShort = 7;
        int myInt = 5234;
        long myLong = 232245;
        float myFloat = 5.99F;
        double myDouble = 2.55;
        char myChar = 'S';
        boolean myBool = true;
    }

    private static void referenceTypes() {
        // Reference Types
        Byte myByte = 6;
        Short myShort = 7;
        Integer myInt = 5234;
        Long myLong = 2322L;
        Float myFloat = 5.99F;
        Double myDouble = 2.55;
        Character myChar = 'S';
        Boolean myBool = true;
        String myString = "Hello World!";
    }
}
